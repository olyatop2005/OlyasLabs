# Исходные данные
salary = 5000  # Ежемесячная зарплата
spend = 6000  # Траты за первый месяц
months = 10  # Количество месяцев, которое планируется протянуть без долгов
increase = 0.03  # Ежемесячный рост цен

# Переменная для подсчета недостающих средств
total_shortage = 0

# Цикл для расчета недостающих средств за 10 месяцев
for month in range(months):
    # Расчет недостающих средств для текущего месяца
    shortage = max(0, spend - salary)
    total_shortage += shortage

    # Увеличиваем расходы на 3%
    spend *= (1 + increase)

# Результат
print(f"Подушка безопасности, чтобы протянуть {months} месяцев без долгов: {round(total_shortage)}")
