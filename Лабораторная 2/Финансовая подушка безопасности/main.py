# Исходные данные
money_capital = 20000  # Подушка безопасности
salary = 5000  # Ежемесячная зарплата
spend = 6000  # Траты за первый месяц
increase = 0.05  # Ежемесячный рост цен

# Переменная для подсчета месяцев
months = 0

while money_capital >= 0:
    # Подсчет бюджета текущего месяца
    budget = salary + money_capital

    # Проверка, можем ли мы покрыть расходы
    if budget >= spend:
        months += 1  # Увеличиваем счетчик месяцев
        # Вычитаем расходы из подушки безопасности
        money_capital -= (spend - salary)
        # Увеличиваем расходы на 5%
        spend *= (1 + increase)
    else:
        break  # Выходим из цикла, если расходы превышают бюджет

print("Количество месяцев, которое можно протянуть без долгов:", months)
